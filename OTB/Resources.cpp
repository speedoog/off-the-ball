#include "Resources.h"

// ********************************************
//	Ctor
// ********************************************
Resources::Resources()
{
}

// ********************************************
//	Dtor
// ********************************************
Resources::~Resources()
{
}

// ********************************************
//	Init
// ********************************************
void Resources::Init()
{
	// load all
	_pFont =new hgeFont("Data/font1.fnt");
	_pFont->SetProportion(-1);

	float scale =-0.01f;
	_pFont->SetScale(scale);
}

// ********************************************
//	Kill
// ********************************************
void Resources::Kill()
{
	// delete all
	delete _pFont;
}
