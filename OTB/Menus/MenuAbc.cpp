#include "MenuAbc.h"

#include "MenuItem.h"
#include "../OTB.h"
#include "../../HGE/hgecolor.h"

// ****************************************************************************************
//	Ctor
// ****************************************************************************************
MenuAbc::MenuAbc()
: _pGUI(NULL)
{
}

// ****************************************************************************************
//	Init
// ****************************************************************************************
void MenuAbc::Init()
{
	_pGUI =new hgeGUI();
	_pGUI->SetNavMode(HGEGUI_UPDOWN | HGEGUI_CYCLED);
//	_pGUI->SetCursor(spr);

	StartMenuMain();
}

// ****************************************************************************************
//	Kill
// ****************************************************************************************
void MenuAbc::Kill()
{
	delete _pGUI;
	_pGUI =NULL;
}

// ****************************************************************************************
//	ClearMenu
// ****************************************************************************************
void MenuAbc::ClearMenu()
{
	_pGUI->Leave();
	_pGUI->Clear();
	_rCurrentY =GetMenuPosY()-0.6f;
}

// ****************************************************************************************
//	AddMenuItem
// ****************************************************************************************
void MenuAbc::AddMenuItem(const UInt32 ItemId, const TString& sLabel)
{
	_pGUI->AddCtrl(new hgeGUIMenuItem(ItemId, this, 0.0f, _rCurrentY, sLabel.GetCharconst()) );
	_rCurrentY -=0.4f;
}

// ****************************************************************************************
//	GetMenuPosY
// ****************************************************************************************
Float32	MenuAbc::GetMenuPosY()
{
	Otb* pOTB =Otb::GetInstance();
	return pOTB->GetGame().GetLevel().GetSize().y*0.75f;
}

// ****************************************************************************************
//	RenderBackground
// ****************************************************************************************
void MenuAbc::RenderBackground()
{
	Otb* pOTB =Otb::GetInstance();
	Otb::RenderQuad( hgeVector(-50.0f, -50.0f), hgeVector(50.0f, 50.0f), hgeColorRGB(0.0f, 0.0f, 0.0f, 0.4f), pOTB->GetResources()._texPowerBar);
	Otb::RenderQuad( hgeVector(-3.0f, 2.0f), hgeVector(3.0f, 7.0f), hgeColorRGB(0.0f, 0.0f, 0.63f, 0.4f), pOTB->GetResources()._texPowerBar);
}
